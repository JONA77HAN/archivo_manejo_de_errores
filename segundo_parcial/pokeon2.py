# Función para obtener la información de un Pokémon
def obtener_info_pokemon(nombre):
    nombre = nombre.lower()
    
    # Buscamos el Pokémon en el diccionario
    for numero, pokemon in pokemons_kanto.items():
        if pokemon['nombre'].lower() == nombre:
            print(f"Pokémon: {pokemon['nombre']}")
            print(f"Número Nacional: {numero}")
            print(f"Tipos: {', '.join(pokemon['tipos'])}")
            return
    
    # Si el Pokémon no está en la lista, mostramos un mensaje de error
    print("Pokémon no encontrado. Por favor, verifica el nombre e intenta de nuevo.")

# Menú principal
def menu():
    print("Bienvenido al sistema de información de Pokémon de Kanto!")
    while True:
        print("\nSelecciona una opción:")
        print("1. Obtener información de un Pokémon específico")
        print("2. Salir")
        
        opcion = input("Ingrese el número de opción: ")
        
        if opcion == '1':
            pokemon_nombre = input("Introduce el nombre del Pokémon: ")
            obtener_info_pokemon(pokemon_nombre)
        elif opcion == '2':
            print("¡Hasta luego!")
            break
        else:
            print("Opción inválida. Intenta de nuevo.")

if __name__ == "__main__":
    menu()
